#Setting the Data Type
#Print the data type of the variable x:
x = 5
print(type(x))




#Setting the Specific Data Type

#String
x = str("Hello World")
print(x)#display x:
print(type(x)) #display the data type of x:

#int
x = int(20)
print(x)         #display x:
print(type(x))   #display the data type of x:

#float
x = float(20.5)
print(x)          #display x:
print(type(x))    #display the data type of x:

#complex
x = complex(1j)
print(x)#display x:
print(type(x))#display the data type of x:

#list
x = list(("apple", "banana", "cherry"))
print(x)#display x:
print(type(x)) #display the data type of x:

#tuple
x = tuple(("apple", "banana", "cherry"))
print(x)#display x:
print(type(x))#display the data type of x:

#range
x = range(6)
print(x)#display x:
print(type(x))#display the data type of x:

#dict
x = dict(name ="John", age=36)
print(x)#display x:
print(type(x)) #display the data type of x:

#Set
x = set(("apple", "banana", "cherry"))
print(x)#display x:
print(type(x)) #display the data type of x:

#frozenset
x = frozenset(("apple", "banana", "cherry"))
print(x)#display x:
print(type(x))#display the data type of x:

#bool
x = bool(5)
print(x)#display x:
print(type(x)) #display the data type of x:

#bytes
x = bytes(5)
print(x)#display x
print(type(x)) #display the data type of x:

#bytearray
x = bytearray(5)
print(x)#display x:
print(type(x))#display the data type of x:

#memoryview
x = memoryview(bytes(5))
print(x)#display x:
print(type(x)) #display the data type of x:

